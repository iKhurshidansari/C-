#include<iostream>
#include<math.h> 
using namespace std;
 
int main(){
    cout << sqrt(16) << endl;// 4

    cout << pow(2,5) << endl;// 32
    cout << round(4.6) << endl;// 5
    cout << ceil(4.1) << endl;// 5
    cout << floor(4.9) << endl;// 4
    cout << abs(-4) << endl;// 4
    cout << fmax(4.5, 5.5) << endl;// 5.5
    cout << fmin(4.5, 5.5) << endl;// 4.5
    cout << fmod(5.5, 2.0) << endl;// 1.5
    cout << cbrt(27.0) << endl;// 3
    return 0;
}

