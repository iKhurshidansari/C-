#include<iostream>
using namespace std;

int main(){
    //int Data Type
    int a = 5;

    //float Data Type
    float b = 3.14;

    //char Data Type
    char c = 'a';
    //C++ allows you to use single quotes for single characters and double quotes for strings.
    string str = "Hello";

    //bool Data Type
    bool isPrime = true; //Gives output 1 if true and 0 if false

    cout << (bool)123 << endl; // Output : 1
    cout << (bool)-123 << endl; // Output : 1
    cout << (bool)0 << endl; // Output : 0

    //Special Data Type : void (Will be covered Later)

    //String is a Derived DataType
    string str = "1234566";
    //String is not a primitive data type, it is a sequence of characters.
    //String is a part of C++ Standard Library.
}